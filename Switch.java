package First_package;

public class Switch {
	public static void main(String args[]) {
		String grade="A";
		 switch(grade) {
			 case"A":
				 System.out.println("Exellent student");
				 break;
			 case "B":
				 System.out.println("Good student");
				 break;
			 case "C":
				 System.out.println("Poor student");
				 break;
			default:
				System.out.println("Failed student");
				break;
		 
		 }	
	}
}
